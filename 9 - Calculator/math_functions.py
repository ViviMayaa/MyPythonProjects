
class MathFunctions():
    """Has calculation functions to be called inside"""
    @classmethod
    def addition(cls, a, b):
        return a + b

    @classmethod
    def subtraction(cls, a, b):
        return a - b

    @classmethod
    def division(cls, a, b):
        return a / b

    @classmethod
    def multiplication(cls, a, b):
        return a * b

